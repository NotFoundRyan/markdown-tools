const langNames: Record<Language, string> = {
  zh: '中文',
  en: 'English',
  ja: '日本語',
  fr: 'Français',
  es: 'Español',
  de: 'Deutsch',
  ru: 'Русский',
};

const langHtmlAttrs: Record<Language, string> = {
  zh: 'zh-CN',
  en: 'en',
  ja: 'ja',
  fr: 'fr',
  es: 'es',
  de: 'de',
  ru: 'ru',
};

let currentLang: Language = 'zh';

export function getStoredLang(): Language {
  if (typeof window === 'undefined') return 'zh';
  const stored = localStorage.getItem(LANG_KEY);
  if (stored && isValidLang(stored)) {
    return stored;
  }

  const browserLang = navigator.language.toLowerCase();
  if (browserLang.startsWith('zh')) return 'zh';
  if (browserLang.startsWith('ja')) return 'ja';
  if (browserLang.startsWith('fr')) return 'fr';
  if (browserLang.startsWith('es')) return 'es';
  if (browserLang.startsWith('de')) return 'de';
  if (browserLang.startsWith('ru')) return 'ru';
  return 'en';
}

function isValidLang(lang: string): lang is Language {
  return ['zh', 'en', 'ja', 'fr', 'es', 'de', 'ru'].includes(lang);
}

export function setLang(lang: Language) {
  currentLang = lang;
  localStorage.setItem(LANG_KEY, lang);
  document.documentElement.setAttribute('lang', langHtmlAttrs[lang]);

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (key) {
      el.textContent = t(key);
    }
  });
}

export function t(key: string): string {
  return translations[currentLang][key] || key;
}

export function getCurrentLang(): Language {
  return currentLang;
}

export function getAllLanguages(): { code: Language; name: string }[] {
  return Object.entries(langNames).map(([code, name]) => ({
    code: code as Language,
    name,
  }));
}

export function initI18n() {
  currentLang = getStoredLang();
  document.documentElement.setAttribute('lang', langHtmlAttrs[currentLang]);
}
